const contractAddress = '0x8Bbf08B5E9F88F8CAdb0d4760b1C60E25edaFba1';

const usdtAddress = "0xC6117086f09Ec2b4111af54a372DE4e391296B52"
// Function to connect to MetaMask
async function connectMetaMask() {
  if (window.ethereum) {
    try {
      // Prompt user for account connections
      await window.ethereum.request({ method: 'eth_requestAccounts' });

      // Create an ethers provider
      const provider = new ethers.providers.Web3Provider(window.ethereum);

      // Get the signer
      signer = provider.getSigner();

      // Get the user's account
      account = await signer.getAddress();
      console.log('Connected account:', account);

      // Initialize contract
      contract = new ethers.Contract(contractAddress, contractABI, signer);

      usdt = new ethers.Contract(usdtAddress, usdtAbi, signer);

      document.getElementById('connectButton').innerText = 'MetaMask Connected';
    } catch (error) {
      console.error('User denied account access:', error);
    }
  } else {
    console.error('MetaMask is not installed.');
    alert('Please install MetaMask to use this feature.');
  }
}

document.getElementById('connectButton').addEventListener('click', connectMetaMask);

// Function to sign typed data
async function signTypedData() {
  if (!signer) {
    console.error('Please connect to MetaMask first.');
    alert('Please connect to MetaMask first.');
    return;
  }

  // EIP-712 Domain
  const domain = {
    name: 'SimplyPay',
    version: '1.0',
    chainId: 11155111, // Sepolia chain ID
    verifyingContract: "0xb6AaedF2A0636fe08d9EF52D58dbB54EEebDF8E9",
}


  // Types as per EIP-712
  const types = {
    PaymentRequest: [
        {name: "sender", type:"address"},
        {name: "amount", type:"uint256"},
        {name: "nonce", type:"uint256"},
        {name: "expire", type:"uint256"},
    ],
}

// const currentNonce = await simplyPay.nonces(signer);
// const nonce = ethers.toNumber(currentNonce) + 1;

// Get the current time in seconds since Unix epoch
const currentTime = Math.floor(Date.now() / 1000);

expireTime = currentTime + 5 * 60; 

const message = {
    sender: account,
    amount: ethers.utils.parseUnits("10").toString(),
    nonce: 1,
    expire: expireTime
}

  try {
    // Sign the typed data
    const signature = await signer._signTypedData(domain, types, message);
    console.log('Signature:', signature);

    // Display the signature
    document.getElementById('signature').innerText = `Signature: ${signature}`;
  } catch (error) {
    console.error('Error signing typed data:', error);
    alert('Error signing typed data. Check console for details.');
  }
}

// Function to deposit
async function deposit() {
    // Ensure MetaMask is connected
    if (!contract) {
      await connectMetaMask();
    }
  
    // Get the deposit amount from the input field
    const amountInput = document.getElementById('amount').value;
    if (!amountInput || isNaN(amountInput)) {
      document.getElementById('message').innerText = 'Please enter a valid amount.';
      return;
    }
  
    // Convert the amount to Wei
    const amount = ethers.utils.parseUnits(amountInput.toString(), 6);
    console.log(amount);
  
    try {
        const gasLimit = await usdt.estimateGas.approve(account, amount).catch(() => {
            // If gas estimation fails, use a reasonable default
            return ethers.BigNumber.from("100000");
          });
     const tx = await usdt.approve(contractAddress, amount, {
        gasLimit: gasLimit
     });

     await tx.wait();
      // Send transaction to simplyPay function
      const txx = await contract.deposit(amount);
  
      document.getElementById('message').innerText = 'Transaction sent. Waiting for confirmation...';
  
      // Wait for transaction to be mined
      await txx.wait();
  
      document.getElementById('message').innerText = 'Deposit successful!';
      console.log('Transaction confirmed:', tx);
    } catch (error) {
      console.error('Error during deposit:', error);
      document.getElementById('message').innerText = 'Error during deposit. See console for details.';
    }
  }
  
document.getElementById('depositButton').addEventListener('click', deposit);

document.getElementById('signButton').addEventListener('click', signTypedData);
